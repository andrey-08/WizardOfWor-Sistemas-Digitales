static void move_Player(int d, int m[53][90]){
	
	//mover jugador hacia abajo
	if (d==0){
		
		if (player.y>= 350){
			player.y=350;
		}
		else{
			if (collision_wall_down_player(player, m) == 0){
				player.y+=5;
			}
		}
	}
	
	//Mover jugador hacia arriba
	if(d==1){
		if (player.y<=20){
			player.y=20;
		}
		else {
			if (collision_wall_up_player(player, m) == 0){
				player.y -= 5;
			}
		}
		
	}
		

	//Mover jugador hacia la derechaa
	if(d==2){
		
		if(player.x>=790){
			player.x=790;
			if ((player.y >= 110) && (player.y <=160)){
					player.y=120;
					player.x=20;
			}	
		}
		else {
			if (collision_wall_right_player(player, m) == 0){
				player.x+=5;
			}
		}
	}
	
	//Mover hacia la izquierda
	if(d==3){
		if(player.x<=70){
			player.x=70;
			if ((player.y >= 110) && (player.y <=160)){
				player.y=120;
				player.x=830;			
			}
		}

		else{
			if (collision_wall_left_player(player, m) == 0){
			player.x-=5;
			}
		}
	}
}

